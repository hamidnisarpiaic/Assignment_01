    // Addition: 4 + 4 = 8
    const additionResult = 4 + 4;
    console.log("Addition:", additionResult);
  
    // Subtraction: 10 - 2 = 8
    const subtractionResult = 10 - 2;
    console.log("Subtraction:", subtractionResult);
  
